schaffer6 <- function(xx) {
    ##########################################################################
    #
    # INPUT:
    #
    # xx = c(x1, x2, ..., xd)
    #
    ##########################################################################
    a <- xx[1];
    b <- xx[2];
    y <- 0.5 + (sin(sqrt(a*a + b*b))^2 - 0.5)/(1 + 0.001 * (a*a + b*b))^2
    return(y)
}

griewank <- function(xx) {
    ##########################################################################
    #
    # INPUT:
    #
    # xx = c(x1, x2, ..., xd)
    #
    ##########################################################################
    
    ii <- c(1:length(xx))
    sum <- sum(xx^2/4000)
    prod <- prod(cos(xx/sqrt(ii)))
    
    y <- sum - prod + 1
    return(y)
}

rastrigin <- function(xx) {
    ##########################################################################
    #
    # INPUT:
    #
    # xx = c(x1, x2, ..., xd)
    #
    ##########################################################################
    
    d <- length(xx)
    
    sum <- sum(xx^2 - 10*cos(2*pi*xx))
    
    y <- 10*d + sum
    return(y)
}

rosenbrock <- function(xx) {
    ##########################################################################
    #
    # INPUT:
    #
    # xx = c(x1, x2, ..., xd)
    #
    ##########################################################################
    
    d <- length(xx)
    xi <- xx[1:(d-1)]
    xnext <- xx[2:d]
    
    sum <- sum(100*(xnext-xi^2)^2 + (xi-1)^2)
    
    y <- sum
    return(y)
}

sphere <- function(xx) {
    ##########################################################################
    #
    # INPUT:
    #
    # xx = c(x1, x2, ..., xd)
    #
    ##########################################################################
    d <- length(xx)
    xi <- xx[1:d]
    sum <- sum(xi*xi)
    return(sum)
}

goldenprice <- function(xx) {
    ##########################################################################
    #
    # INPUT:
    #
    # xx = c(x1, x2)
    #
    ##########################################################################

    x1 <- xx[1]
    x2 <- xx[2]
      
    fact1a <- (x1 + x2 + 1)^2
    fact1b <- 19 - 14*x1 + 3*x1^2 - 14*x2 + 6*x1*x2 + 3*x2^2
    fact1 <- 1 + fact1a*fact1b
      
    fact2a <- (2*x1 - 3*x2)^2
    fact2b <- 18 - 32*x1 + 12*x1^2 + 48*x2 - 36*x1*x2 + 27*x2^2
    fact2 <- 30 + fact2a*fact2b
      
    y <- fact1*fact2
    return(y)
}